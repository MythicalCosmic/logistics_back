class Command(BaseCommand):
    help = "Seed all 50 US states"

    def handle(self, *args, **options):
        created = 0
        for abbr, name in US_STATES:
            _, was_created = State.objects.get_or_create(
                abbreviation=abbr,
                defaults={"name": name},
            )
            if was_created:
                created += 1

        self.stdout.write(self.style.SUCCESS(
            f"Done. {created} states created, {State.objects.count()} total"
        ))
